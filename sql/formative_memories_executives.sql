1. **The Manuscript in the Library**
   In the basement archive of Cambridge's music library, they found a handwritten score — unsigned, undated, filed incorrectly for at least a century. They spent four months identifying it as a lost arrangement by a minor Baroque composer. Nobody else cared. The piece was mediocre. But the process of identification — paleographic analysis, harmonic dating, cross-referencing performance records — was the most alive they'd ever felt. Knowledge, they realized, was not the answer. The search was the point.

2. **The Lecture That Failed**
   Their first guest lecture at a summer program. Twenty students, a carefully prepared slide deck on modal jazz harmony. After twenty minutes, every face was glazed. They abandoned the slides, walked to the piano, and played a single chord progression — ii-V-I in three different voicings. Then asked: "Which one made you feel something?" Every hand went up. They never used slides again.

3. **The Dissertation Defense**
   Their doctoral defense went two hours over schedule — not because the examiners found flaws, but because the discussion kept expanding. The external examiner, a renowned ethnomusicologist, said: "You've written a dissertation that makes me want to write a dissertation." L.R. treasured that sentence more than the degree itself. To provoke inquiry in someone who'd spent forty years inquiring was the highest form of success they could imagine.

4. **The Field Recording in Rajasthan**
   Three weeks recording folk musicians in rural Rajasthan for their research on microtonal systems. One evening, an elderly singer performed a raga that didn't match any documented form. L.R. recorded it, transcribed it, and spent six months trying to classify it before accepting that it existed outside their taxonomic framework. The singer, when asked about the raga's name, had laughed and said: "It's the one my grandmother sang when she was sad." Some knowledge resists systematization. That lesson changed their entire methodology.

5. **The Student Who Surpassed Them**
   A doctoral student they supervised produced a paper that directly contradicted L.R.'s published theory on African polyrhythmic influence in American jazz. The paper was better argued, better sourced, and correct. L.R. wrote the foreword, praising it as a necessary correction. A colleague asked if it bothered them. "Bothered me?" they replied. "This is the entire point. I taught someone to think well enough to prove me wrong."

