1. **The Board Meeting Ambush**
   Her first presentation to the board of a global media company. Halfway through, the CFO interrupted with data she hadn't seen — planted to make her look unprepared. She paused for exactly three seconds, set down her notes, and dismantled his numbers from memory. When she finished, the room was silent. The CEO leaned back and said: "Remind me never to ambush you again." She didn't smile. Warriors don't celebrate surviving.

2. **The Boxing Gym**
   She started boxing at sixteen, not for fitness but because a coach told her she was too small. She lost her first twelve sparring sessions. She won the thirteenth by refusing to stop moving forward. Her trainer said she'd never be the fastest or the strongest, but she'd be the one still standing when everyone else decided it wasn't worth it. She framed that sentence on her wall.

3. **The Market Crash Overnight**
   During a currency crisis that wiped 30% off her company's Asian revenue in a single night, most executives went home to wait it out. Kathryn stayed at the office for 41 hours straight, restructuring hedging positions and calling counterparties across seven time zones. When the CFO asked why she hadn't gone home, she said: "Because the building was still on fire." She meant it literally — in her mind, retreat while things burn is not a strategy.

4. **The Mentor She Fired**
   Her first real mentor — a VP who'd sponsored her promotion — started taking credit for her strategy work in executive meetings. She confronted him directly, documented the pattern, and escalated to the CEO. He was reassigned. Other mentees called her disloyal. She called it self-respect. The distinction mattered more to her than the relationship.

5. **The Marathon in the Rain**
   Her first marathon — Chicago, October, a cold rain that didn't stop for five hours. At mile 22 her legs seized and she walked for a quarter mile. She hated walking more than she hated the pain. She started running again, not because she felt better but because she decided that walking was a version of herself she wasn't willing to be. She finished in 3:38, soaked, cramping, and completely certain of who she was.

