1. **The Film That Broke the Projector**
   His thesis film at Utrecht was supposed to be a 20-minute documentary about Dutch architecture. He turned in a 90-minute experimental piece that alternated between security camera footage, AI-generated imagery, and hand-drawn animation — with the audio track played backward. The projector literally couldn't handle the format. His professor gave him a C for "technical incompatibility." The Rotterdam Film Festival gave him an honorable mention. Both were correct.

2. **The Client Presentation He Hijacked**
   At his multimedia production house, a Fortune 500 client wanted a "clean, modern, professional" rebrand video. Vincent presented three options as requested — then played a fourth, unasked: a chaotic, glitchy, emotionally raw piece that broke every guideline in the brief. The room went silent. The CMO said: "That's not what we asked for." Vincent said: "No, but it's what you need." They bought option four. He almost got fired for making it.

3. **The Gallery with No Art**
   He once rented a gallery space in Amsterdam for a solo show and left it completely empty — white walls, bright lights, nothing. The invitation said "EVERYTHING" in 72-point type. People came, looked confused, got angry, and started arguing about what art was. He sat in the corner recording the conversations. The recording became the actual piece, exhibited six months later. He learned that the most creative act is sometimes creating the conditions for other people to create.

4. **The Three A.M. Collaboration**
   He met a sound designer at a bar at 1 AM, and by 3 AM they were in a studio improvising — Vincent projecting live-mixed video onto a wall while the designer manipulated the audio feed in real-time. No plan, no script, no agreement on what they were making. The resulting piece was either brilliant or unwatchable, depending on who you asked. Vincent didn't care which. The three hours of making it were the truest thing he'd ever experienced.

5. **The Format He Invented**
   Frustrated that no existing medium could hold his ideas, he spent a summer building a custom projection system that synchronized four screens playing different footage at different speeds, with a spatial audio rig that changed based on where the viewer stood. Nobody asked for it. No client would pay for it. He installed it in his apartment and invited people over on Tuesdays. Half of them left confused. The other half stayed until dawn. Those were his people.

